#Exercício 4 - Calcular e exibir a média aritmética de quatro valores quaisquer que serão digitados

print('------ Calculando média aritmética ------')
n1 = float(input('Informe o primeiro número: '))
n2 = float(input('Informe o segundo número: '))
n3 = float(input('Informe o terceiro número: '))
n4 = float(input('Informe o quarto número: '))

media = (n1 + n2 + n3 + n4)/ 4

print('A média é: ', media)