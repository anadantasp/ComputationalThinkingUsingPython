#Exercício 3 - A partir dos valores da base e altura de um triângulo, calcular e exibir sua área

print('------ Calculando a área de um triângulo ------')
base = int(input('Base do triângulo: '))
altura = int(input('Altura do triângulo: '))

area = (base * altura) / 2

print('A área do triangulo é: ', area)